vi g[N];

void solve(int T){
    int n, m, k;
    cin >> n >> m >> k;
    for(int i = 0; i < m; ++i){
        int a, b;
        cin >> a >> b;
        g[a-1].push_back(b-1);
        g[b-1].push_back(a-1);
    }

    vi dp(n);
    dp[0] = 1;

    for(int i = 1; i <= k; ++i){
        LL s = 0;
        for(int j = 0; j < n; ++j) 
            s += dp[j];
        vi next_dp(n);
        for(int j = 0; j < n; ++j){
            next_dp[j] = s - dp[j]; // self
            for(int v: g[j]){ // unusable
                next_dp[j] -= dp[v];
            }
            next_dp[j] %= mod;
        }
        swap(dp, next_dp);
    }

    cout << dp[0] << "\n";
}   
 
