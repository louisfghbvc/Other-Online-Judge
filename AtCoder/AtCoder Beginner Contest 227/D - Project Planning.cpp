void solve(int T){
    int n, k;
    cin >> n >> k;

    vi arr(n);
    cin >> arr;

    auto good = [&](LL x){
        LL sum = 0;
        for(auto &v: arr){
            sum += min(v, x);
        }
        return sum >= x * k;
    };

    LL r = 1;
    while(good(r)) r <<= 1;

    LL l = 0;
    LL ans = 0;
    while(l <= r){
        LL mid = (l+r)>>1;
        if(good(mid)) ans = mid, l = mid+1;
        else r = mid-1;
    }
    cout << ans << "\n";
}   
 
