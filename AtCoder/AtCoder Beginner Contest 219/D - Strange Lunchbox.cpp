int A[N], B[N];

int n, x, y;
int dp[N][N][N];

int dfs(int i, int a, int b){
    if(a <= 0 && b <= 0) return 0;
    if(i >= n) return 1e9;
    if(dp[i][a][b] != -1) return dp[i][a][b];
    int res = 1 + dfs(i+1, max(a - A[i], 0), max(b - B[i], 0));
    res = min(res, dfs(i+1, a, b));
    return dp[i][a][b] = res;
}

