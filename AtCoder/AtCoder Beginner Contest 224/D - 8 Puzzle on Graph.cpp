map<string, int> dp;
vi g[10];

void solve(int T){
    int m;
    cin >> m;
    for(int i = 0; i < m; ++i){
        int u, v;
        cin >> u >> v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    string s(9, '9');
    for(int i = 1; i <= 8; ++i){
        int x; cin >> x; x--;
        s[x] = i+'0';
    }
    
    queue<string> q;
    q.push(s);
    dp[s] = 0;
    
    while(q.size()){
        string state = q.front(); q.pop();
        int i;
        for(i = 0; i < 9; ++i) if(state[i] == '9') break;
        for(int v: g[i+1]){
            string next_state = state;
            swap(next_state[i], next_state[v-1]);
            if(dp.count(next_state)) continue;
            dp[next_state] = dp[state] + 1;
            q.push(next_state);
        }

    }

    if(!dp.count("123456789")) cout << "-1\n";
    else cout << dp["123456789"] << "\n";
}   
 
