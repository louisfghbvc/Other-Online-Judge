void solve(int T){
    int n;
    cin >> n;

    vi x(n), v(n);
    cin >> x >> v;

    double l = 0, r = 1e9;
    while(r - l > 1e-7){
        double mid = (l+r)/2;
        double L = x[0] - mid * v[0];
        double R = x[0] + mid * v[0];
        for(int i = 1; i < n; ++i){
            L = max(L, x[i] - mid*v[i]);
            R = min(R, x[i] + mid*v[i]);
        }
        if(L <= R) r = mid;
        else l = mid;
    }
    cout << setprecision(10) << l << "\n";
}   
 
